package JavaPackage;

public class AssignmentOperator {

	public static void main(String[] args) {
		int a=10;//local variable
		
		a+=3;
		System.out.println(a);//13
		
		a*=4;
		System.out.println(a);//52
		
		a-=5;
		System.out.println(a);//47
		
		a/=5;
		System.out.println(a);//9
}
}