public class Foreachloop {

public static void main(String[] args) {
	
	//Defining an array
	int a[] = {1,2,3,4,5,6,7,8,9};
	
	//printing an array using for each loop
	//Associating array to the new varaiable b
	for(int b:a)
	{
		System.out.println(b);
	}
}
}
